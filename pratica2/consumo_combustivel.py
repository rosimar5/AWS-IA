# 4- Calculadora de Consumo de Combustível
# Desenvolva um programa que calcula o consumo médio de combustível de um veículo. Use os seguintes dados:

# * Distância percorrida: 300 km
# * Combustível gasto: 25 litros
# O programa deve calcular o consumo médio (km/l) e exibir todos os dados da viagem, incluindo o resultado final arredondado para duas casas decimais.

distancia_percorrida = 300
combustivel_gasto = 25

media_consumo_combustivel = distancia_percorrida / combustivel_gasto

print(f"Distancia Percorrida: {distancia_percorrida}")
print(f"Combustivel Gasto: {combustivel_gasto}")
print(f"Média de Quilômetro por Litro: {media_consumo_combustivel}")